/**
 * @author Exrickx
 */
@Data
@Configuration
@ConfigurationProperties(prefix = "scmt.app-token")
public class ScmtAppTokenProperties {

    /**
     * 单平台登陆
     */
    private Boolean spl = true;

    /**
     * token过期时间（天）
     */
    private Integer tokenExpireTime = 30;
}
